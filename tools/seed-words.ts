async function seedWords() {
  console.log(`Seeding ${WORDS.length} words...`);

  // Insert in batches of 500
  const batchSize = 500;
  let inserted = 0;

  for (let i = 0; i < WORDS.length; i += batchSize) {
    const batch = WORDS.slice(i, i + batchSize).map(word => ({ word }));

    const { error } = await supabase
      .from('words')
      .upsert(batch, { onConflict: 'word' });

    if (error) {
      console.error(`Error inserting batch ${i / batchSize}:`, error);
    } else {
      inserted += batch.length;
      console.log(`Inserted ${inserted}/${WORDS.length} words`);
    }
  }

  console.log('Done seeding words!');
}

