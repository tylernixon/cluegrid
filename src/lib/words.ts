/**
 * Check if a word is a valid English word
 */
export function isValidWord(word: string): boolean {
  return VALID_WORDS.has(word.toUpperCase());
}

/**
 * Add words to the valid set (for puzzle answers that might not be in base list)
 */
export function addValidWords(words: string[]): void {
  for (const word of words) {
    VALID_WORDS.add(word.toUpperCase());
  }
}
