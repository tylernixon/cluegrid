INSERT INTO words (word, frequency_rank, is_valid_guess, is_valid_answer, categories)
SELECT DISTINCT ON (s.word)
    s.word,
    s.rank,
    true,                                                      -- all words are valid guesses
    CASE WHEN s.rank <= 2000 THEN true ELSE false END,         -- top 2000 are valid answers
    CASE
        WHEN s.rank <= 500  THEN ARRAY['common', 'tier1']
        WHEN s.rank <= 2000 THEN ARRAY['common', 'tier2']
        ELSE                     ARRAY['extended', 'tier3']
    END
FROM _seed_words s
ORDER BY s.word, s.rank ASC
ON CONFLICT (word) DO UPDATE SET
    frequency_rank  = EXCLUDED.frequency_rank,
    is_valid_guess  = EXCLUDED.is_valid_guess,
    is_valid_answer = EXCLUDED.is_valid_answer,
    categories      = EXCLUDED.categories;

